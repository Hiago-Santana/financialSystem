export function translateCategory(category) {
  let result;
  switch (category) {
    case "work":
      return result = "Trabalho";
    case "alimentanction":
      return result = "Alimentação";
    case "transport":
      return result = "Transporte";
    case "accounts":
      return result = "Contas";
    case "health":
      return result = "Saúde";
    case "investment":
      return result = "Investimento";
    case "leisure":
      return result = "Lazer";
    case "education":
      return result = "Educação";
    case "other":
      return result = "Outros";
  }
}
